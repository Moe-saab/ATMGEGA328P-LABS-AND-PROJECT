.INCLUDE "m328pdef.inc"
.ORG 0X0000
	RJMP init

init:
SBI DDRB,3        ;;pin pb3 output
CBI PORTB,3       
SBI DDRB,4     ;;pin pb4 output
CBI PORTB,4          
SBI DDRB,5         ;;pin pb5 output
CBI PORTB,5  

main:
;;rows 1,3,5,7
LDI R16,0X50
LOOP1:	SBI PINB,3
		CBI PORTB,5
		SBI PORTB,5
		DEC R16
		BRNE LOOP1

LOOP2:
LDI R17,0x08
LDI R18,0b01010101
ROL_LOOP:
CBI PORTB,3
ROL R18
BRCC ZEROCARRY
SBI PORTB,3 ;;CARRY NOT ZERO
ZEROCARRY: CBI PORTB,5
		   SBI PORTB,5
DEC R17
BRNE ROL_LOOP


CBI PORTB,4
SBI PORTB,4
CBI PORTB,4



;;rows 2,4,6
LDI R16,0X50
SBI PORTB,3
LOOP3:	SBI PINB,3
		CBI PORTB,5
		SBI PORTB,5
		DEC R16
		BRNE LOOP3

LOOP4:
LDI R17,0x08
LDI R18,0b10101010
ROL_LOOP2:
CBI PORTB,3
ROL R18
BRCC ZEROCARRY2
SBI PORTB,3 ;;CARRY NOT ZERO
ZEROCARRY2: CBI PORTB,5
		   SBI PORTB,5
DEC R17
BRNE ROL_LOOP2


CBI PORTB,4
SBI PORTB,4
CBI PORTB,4



RJMP main
