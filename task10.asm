.INCLUDE "m328pdef.inc"
.ORG 0X0000
	RJMP init

init:
;Initialization related to screen
SBI DDRB,3        
CBI PORTB,3       
SBI DDRB,4	     
CBI PORTB,4          
SBI DDRB,5       
CBI PORTB,5  
LDI R17,0X01

main:
;fill the character buffer with what you want to display from the chartable
LDI YH,high(0x0100)
LDI YL,low(0x0100)
LDI R16,0
LDI R19,0X10 ;COUNTER FOR THE 16 BLOCKS
;MAKE LOOP OF 16 TIMES TO FILL THE CHAR BUFFER WITH VALUES 
CHARBUFFERFILLING:
ST Y+,R16
INC R16
DEC R19 
BRNE CHARBUFFERFILLING


SCREEN:
;send column data
;init charbuffer pointer
LDI YH,high(0x110)
LDI YL,low(0x110)
LDI R18,16
;code for 1 block
BlockLoop:
;calculate offset in table depending on charvalue
LD R20,-Y
LDI ZH,high(Chartable<<1)
LDI ZL,low(Chartable<<1)
LDI R21,8
MUL R20,R21
ADD ZL,R0
;now we still have offset of row +(rownumber -1)
;i know that initially r17=0x01 refers to row 8 and each time it shifts and this means row decrease by 1
RJMP BYTEOFFSET
ADDBYTEOFFSET:
ADD ZL,R22 ;FIX LATER
;Load column data
LPM R20,Z
LDI R21,5
CLC
BlockColumnLoop:
CBI PORTB,3
ROR R20
BRCC ZEROCARRY
SBI PORTB,3 ;;CARRY NOT ZERO
ZEROCARRY: 
CBI PORTB,5
SBI PORTB,5
DEC R21
BRNE BlockColumnLoop
;When this is finished start with next block
DEC R18
BRNE BlockLoop

;send row data
LDI R18,8
CLC
ROR_LOOP8:
CBI PORTB,3
ROR R17
BRCC CARRYISZERO
CARRYISONE: SBI PORTB,3
CARRYISZERO:
CBI PORTB,5
SBI PORTB,5
DEC R18
BRNE ROR_LOOP8
CBI PORTB,4
SBI PORTB,4
;here I sent a full row now complete
CBI PORTB,4
TST R17
BRNE AGAIN
ROL R17
AGAIN:RJMP SCREEN


