RJMP COMPLETEOFFSET
FOUR:
CPI R24,4
BRNE FIVE
LDI ZH,high(Chartable_haa<<1)
LDI ZL,low(Chartable_haa<<1)
RJMP COMPLETEOFFSET
FIVE:
CPI R24,5
BRNE SIX
LDI ZH,high(Chartable_zaa<<1)
LDI ZL,low(Chartable_zaa<<1)
RJMP COMPLETEOFFSET
SIX:
CPI R24,6
BRNE SEVEN
LDI ZH,high(Chartable_waw<<1)
LDI ZL,low(Chartable_waw<<1)
RJMP COMPLETEOFFSET
SEVEN:
CPI R24,7
BRNE EIGHT
LDI ZH,high(Chartable_dal<<1)
LDI ZL,low(Chartable_dal<<1)
RJMP COMPLETEOFFSET
EIGHT:
CPI R24,8
BRNE NINE
LDI ZH,high(Chartable_jaa<<1)
LDI ZL,low(Chartable_jaa<<1)
RJMP COMPLETEOFFSET
NINE:
CPI R24,9
BRNE A
LDI ZH,high(Chartable_baa<<1)
LDI ZL,low(Chartable_baa<<1)
RJMP COMPLETEOFFSET
A:
CPI R24,10
BRNE B
LDI ZH,high(Chartable_kaaf<<1)
LDI ZL,low(Chartable_kaaf<<1)
RJMP COMPLETEOFFSET
B:
CPI R24,11
BRNE C
LDI ZH,high(Chartable_noon<<1)
LDI ZL,low(Chartable_noon<<1)
RJMP COMPLETEOFFSET
C:
CPI R24,12
BRNE D
LDI ZH,high(Chartable_mim<<1)
LDI ZL,low(Chartable_mim<<1)
RJMP COMPLETEOFFSET
D:
CPI R24,13
BRNE E
LDI ZH,high(Chartable_tah<<1)
LDI ZL,low(Chartable_tah<<1)
RJMP COMPLETEOFFSET
E:
CPI R24,14
BRNE F
LDI ZH,high(Chartable_ha<<1)
LDI ZL,low(Chartable_ha<<1)
RJMP COMPLETEOFFSET
F:
CPI R24,15
BRNE COMPLETEOFFSET
LDI ZH,high(Chartable_ALEF<<1)
LDI ZL,low(Chartable_ALEF<<1)
RJMP COMPLETEOFFSET




COMPLETEOFFSET:
LDI R21,8
MUL R20,R21
ADD ZL,R0
;now we still have offset of row +(rownumber -1)
;i know that initially r17=0x01 refers to row 8 and each time it shifts and this means row decrease by 1
RJMP BYTEOFFSET


BYTEOFFSET:
CPI R17,1
BRNE MESH7
LDI R22,7 ;BYTE OFFSET
RJMP ADDBYTEOFFSET

MESH7:
CPI R17,2
BRNE MESH6
LDI R22,6 ;BYTE OFFSET
RJMP ADDBYTEOFFSET

MESH6:
CPI R17,4
BRNE MESH5
LDI R22,5 ;BYTE OFFSET
RJMP ADDBYTEOFFSET

MESH5:
CPI R17,8
BRNE MESH4
LDI R22,4 ;BYTE OFFSET
RJMP ADDBYTEOFFSET

MESH4:
CPI R17,16
BRNE MESH3
LDI R22,3 ;BYTE OFFSET
RJMP ADDBYTEOFFSET

MESH3:
CPI R17,32
BRNE MESH2
LDI R22,2 ;BYTE OFFSET
RJMP ADDBYTEOFFSET

MESH2:
CPI R17,64
BRNE MESH1
LDI R22,1 ;BYTE OFFSET
RJMP ADDBYTEOFFSET

MESH1:
LDI R22,0 ;BYTE OFFSET
RJMP ADDBYTEOFFSET



ADDBYTEOFFSET:
ADD ZL,R22 
;Load column data
LPM R20,Z
LDI R21,5
CLC
BlockColumnLoop:
CBI PORTB,3
ROR R20
BRCC ZEROCARRY
SBI PORTB,3 ;;CARRY NOT ZERO
ZEROCARRY: 
CBI PORTB,5
SBI PORTB,5
DEC R21
BRNE BlockColumnLoop
;When this is finished start with next block
DEC R23
BREQ STARTSENDINGROWDATA
RJMP BlockLoop
;send row data
STARTSENDINGROWDATA:
LDI R23,8
CLC
ROR_LOOP8:
CBI PORTB,3
ROR R17
BRCC CARRYISZERO
CARRYISONE: SBI PORTB,3
CARRYISZERO:
CBI PORTB,5
SBI PORTB,5
DEC R23
BRNE ROR_LOOP8
RJMP DELAY


DELAY:
LDI R20,0X6B
D1:
LDI R21,0X6B
D2:
DEC R21
BRNE D2
DEC R20
BRNE D1
RJMP DISPLAY







DISPLAY:
CBI PORTB,4
SBI PORTB,4
;here I sent a full row now complete
CBI PORTB,4
TST R17
BRNE SCREEN1
ROL R17
OUT TCNT0,R18
RETI
SCREEN1: RJMP SCREEN









