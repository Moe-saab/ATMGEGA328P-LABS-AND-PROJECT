.ORG 0X0000
	RJMP init
.ORG 0X0020
	RJMP saab_interrupt
init:
;Initialization related to screen
SBI DDRB,3        
CBI PORTB,3       
SBI DDRB,4	     
CBI PORTB,4          
SBI DDRB,5       
CBI PORTB,5  
LDI R17,0X01

;fill the character buffer with what you want to display from the chartable
LDI YH,high(0x0100)
LDI YL,low(0x0100)
LDI R16,0
LDI R19,0X10 ;COUNTER FOR THE 16 BLOCKS
;MAKE LOOP OF 16 TIMES TO FILL THE CHAR BUFFER WITH VALUES 
CHARBUFFERFILLING:
ST Y+,R16
INC R16
DEC R19 
BRNE CHARBUFFERFILLING

;initialization related to keyboard
;rows are output sbi and columns are inputs cbi
SBI	DDRD,7 ;OUTPUTS 7--->4
SBI	DDRD,6
SBI	DDRD,5
SBI	DDRD,4
CBI DDRD,3 ;INPUTS 3--->0
CBI DDRD,2
CBI DDRD,1
CBI DDRD,0
;activate pull up resistors for inputs 3--->0 
SBI	PORTD,3
SBI	PORTD,2
SBI	PORTD,1
SBI	PORTD,0

;HERE INITIALIZING TIMER
init2:
sei 
LDI R16,0X04
OUT TCCR0B,R16 
STS TIMSK0,R17 ; 
LDI R18,0xB9 
OUT TCNT0,R18




