saab_interrupt:

SCREEN:
;send column data
;init charbuffer pointer
LDI YH,high(0x110)
LDI YL,low(0x110)
LDI R23,16
;code for 1 block
BlockLoop:
;calculate offset in table depending on charvalue
LD R20,-Y
;first we need to know what table address to load
ZERO:
CPI R24,0
BRNE ONE
LDI ZH,high(Chartable_fa<<1)
LDI ZL,low(Chartable_fa<<1)
RJMP COMPLETEOFFSET
ONE:
CPI R24,1
BRNE TWO
LDI ZH,high(Chartable_lam<<1)
LDI ZL,low(Chartable_lam<<1)
RJMP COMPLETEOFFSET
TWO:
CPI R24,2
BRNE THREE
